const { Markup } = require('telegraf');

/**
 * Handle /start command
 */
exports.handleStart = async (ctx) => {
  try {
    const { apiClient, logger } = ctx;
    const { id, username, first_name, last_name } = ctx.from;
    
    logger.info(`Start command from user ${id} (${username || 'no username'})`);
    
    // Check if user exists in the system
    try {
      // Try to find user by Telegram ID
      const response = await apiClient.get(`/users/telegram/${id}`);
      
      if (response.data && response.data.user) {
        // User exists
        const user = response.data.user;
        
        await ctx.reply(
          `–î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å, ${user.firstName} ${user.lastName}!\n\n` +
          '–í—ã —É–∂–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã –≤ —Å–∏—Å—Ç–µ–º–µ Service Desk. ' +
          '–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—ã –Ω–∏–∂–µ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å –∑–∞—è–≤–∫–∞–º–∏:',
          Markup.keyboard([
            ['üìù –ù–æ–≤–∞—è –∑–∞—è–≤–∫–∞', 'üìã –ú–æ–∏ –∑–∞—è–≤–∫–∏'],
            ['‚ùì –ü–æ–º–æ—â—å']
          ]).resize()
        );
      }
    } catch (error) {
      if (error.response && error.response.status === 404) {
        // User not found, show registration instructions
        await ctx.reply(
          `–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ, ${first_name || ''} ${last_name || ''}!\n\n` +
          '–î–ª—è –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏—è –±–æ—Ç–∞ Service Desk –≤–∞–º –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞—Ç—å—Å—è –≤ —Å–∏—Å—Ç–µ–º–µ. ' +
          '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è —É—á–µ—Ç–Ω–æ–π –∑–∞–ø–∏—Å–∏ –∏ –ø—Ä–∏–≤—è–∑–∫–∏ –≤–∞—à–µ–≥–æ Telegram ID.\n\n' +
          `–í–∞—à Telegram ID: ${id}`
        );
      } else {
        // Other error
        logger.error('Error checking user:', error);
        await ctx.reply(
          '–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ –≤–∞—à–µ–π —É—á–µ—Ç–Ω–æ–π –∑–∞–ø–∏—Å–∏. ' +
          '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ –∏–ª–∏ –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä—É.'
        );
      }
    }
  } catch (error) {
    ctx.logger.error('Error in handleStart:', error);
    await ctx.reply('–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.');
  }
};

